valor_1 = int(input('Informe o 1º valor: '))
valor_2 = int(input('Informe o 2º valor: '))

soma = valor_1 + valor_2
subtracao = valor_1 - valor_2
multiplicacao = valor_1 * valor_2

print(f'A soma entre {valor_1} e {valor_2} é = {soma}')
print(f'A subtracao entre {valor_1} e {valor_2} é = {subtracao}')
print(f'A multiplicação entre {valor_1} e {valor_2} = {multiplicacao}')
