print('--- Diferença entre maior valor para o menor ---')
valor1 = float(input('Digite um valor qualquer: '))
valor2 = float(input('Digite um valor qualquer: '))

if valor1 > valor2:
    print(f' A diferença será de {valor1 - valor2}')
else:
    print(f' A diferença será de {valor2 - valor1}')
